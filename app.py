with tab_review:
    st.markdown("#### 👤 Human review queue")
    pending = db.get_cases("PENDING_REVIEW")
    if pending.empty:
        st.markdown('<div class="op-card" style="color:#898781">No cases awaiting '
                    'review.</div>', unsafe_allow_html=True)
    else:
        ids = pending["id"].tolist()
        case_id = st.selectbox(
            "Pending case", ids,
            format_func=lambda i: f"{i} — "
            f"{pending.set_index('id').loc[i, 'subject']}")
        case = db.get_case(case_id)
        st.text_area("Message", case["body"], height=110, disabled=True,
                     key=f"body_{case_id}")
        cls = Classification(
            request_type=case["request_type"], urgency=case["urgency"],
            confidence=case["confidence"], rationale=case["rationale"],
            sentiment=case["sentiment"],
            suggested_department=case["department"], source=case["class_source"])
        render_reasoning(cls, case["memory_note"], "")
        proposed = db.get_plan(case_id, "proposed")
        render_plan(proposed, json.loads(case["guardrail_flags"] or "[]"),
                    title="📋 Agent's proposed plan")

        st.markdown("##### Operator decision")
        col1, col2 = st.columns(2)
        with col1:
            new_type = st.selectbox("Request type", config.REQUEST_TYPES,
                                    index=config.REQUEST_TYPES.index(
                                        case["request_type"]))
        with col2:
            new_urgency = st.selectbox("Urgency", config.URGENCY_LEVELS,
                                       index=config.URGENCY_LEVELS.index(
                                           case["urgency"]))
        keep = []
        if new_type == case["request_type"] and proposed:
            st.caption("Keep / drop the agent's proposed steps (mandatory steps are "
                       "re-enforced by the guardrail either way):")
            for i, step in enumerate(proposed):
                if st.checkbox(f"`{step.key()}` — {step.reason}", value=True,
                               key=f"keep_{case_id}_{i}"):
                    keep.append(step)
        if st.button("✅ Approve & Execute", type="primary"):
            human_cls = Classification(
                request_type=new_type, urgency=new_urgency, confidence=1.0,
                rationale=f"Human operator confirmed as {new_type}/{new_urgency} "
                          f"(AI suggested {case['request_type']}/{case['urgency']} "
                          f"at {case['confidence']:.2f}).",
                sentiment=case["sentiment"],
                suggested_department=case["department"], source="human_override")
            human_plan = ([PlannedStep(tool=s.tool, reason=s.reason, params=s.params,
                                       origin="human") for s in keep]
                          if keep else None)
            req = IncomingRequest(id=case["id"], channel=case["channel"],
                                  sender=case["sender"], subject=case["subject"],
                                  body=case["body"], received_at=case["received_at"])
            st.session_state.last_run = run_agent_with_status(
                req, human_cls=human_cls, human_plan=human_plan)
            st.rerun()

    st.markdown("#### ⛔ Held for human (urgent cases)")
    held = db.get_cases("HELD_FOR_HUMAN")
    if held.empty:
        st.markdown('<div class="op-card" style="color:#898781">No urgent cases on '
                    'hold.</div>', unsafe_allow_html=True)
    else:
        for _, row in held.iterrows():
            col1, col2 = st.columns([4, 1])
            with col1:
                st.markdown(
                    f'<div class="op-card" style="margin-bottom:8px">'
                    f'<b>{row["id"]}</b> — {row["subject"]} &nbsp; '
                    f'{ui.urgency_pill(row["urgency"])} '
                    f'<span style="color:#898781;font-size:0.85rem">'
                    f'{row["sender"]}</span></div>', unsafe_allow_html=True)
            with col2:
                if st.button("Mark handled → Resolve", key=f"resolve_{row['id']}"):
                    db.update_case(row["id"], status="RESOLVED")
                    st.rerun()

    if st.session_state.last_run and st.session_state.last_run.results:
        last = st.session_state.last_run
        if last.classification.source == "human_override":
            st.markdown(f"#### Agent trace — `{last.request.id}` (human-approved)")
            render_run(last)

with tab_batch:
    st.markdown("#### 📦 Batch processing")
    st.caption(f"Throttled to {config.BATCH_THROTTLE_SECONDS:.0f}s between requests "
               f"to respect the Gemini free-tier rate limit.")
    uploaded = st.file_uploader("Upload CSV (columns: sender, subject, body)",
                                type="csv")
    use_sample = st.button("Load sample batch (10 requests)")
    batch_df = None
    if uploaded is not None:
        try:
            batch_df = pd.read_csv(uploaded)
        except Exception as e:  # noqa: BLE001
            st.error(f"Could not read CSV: {e}")
    elif use_sample:
        batch_df = pd.read_csv(seed_data.CSV_PATH)
    if batch_df is not None:
        missing = {"sender", "subject", "body"} - set(batch_df.columns)
        if missing:
            st.error(f"CSV is missing required columns: {sorted(missing)}")
        else:
            st.session_state.batch_df = batch_df
    if "batch_df" in st.session_state:
        st.dataframe(st.session_state.batch_df, width="stretch", height=200)
        if st.button("⚙️ Process batch", type="primary"):
            rows = st.session_state.batch_df.to_dict("records")
            progress = st.progress(0.0, text="Starting batch…")
            summary = []
            for i, row in enumerate(rows):
                req = IncomingRequest(id=new_request_id(), channel="batch",
                                      sender=str(row["sender"]),
                                      subject=str(row["subject"]),
                                      body=str(row["body"]), received_at=now_iso())
                progress.progress((i + 0.5) / len(rows),
                                  text=f"Processing {i + 1}/{len(rows)}: "
                                       f"{req.subject[:50]}")
                run = agent.process_request(req, autonomy_mode=autonomy_mode,
                                            critic_enabled=critic_enabled)
                summary.append({
                    "id": req.id, "subject": req.subject[:50],
                    "type": run.classification.request_type,
                    "urgency": run.classification.urgency,
                    "confidence": round(run.classification.confidence, 2),
                    "status": str(run.final_status).replace("CaseStatus.", ""),
                    "flags": "; ".join(run.guardrail_flags) or "—"})
                progress.progress((i + 1) / len(rows))
                if gemini_client.api_available() and i < len(rows) - 1:
                    time.sleep(config.BATCH_THROTTLE_SECONDS)
            progress.progress(1.0, text="Batch complete")
            st.success(f"Processed {len(summary)} requests.")
            st.dataframe(pd.DataFrame(summary), width="stretch")

