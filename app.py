# ---------------------------------------------------------------- trace rendering


def render_reasoning(cls: Classification, memory_note: str, memory_impact: str):
    chips = " &nbsp; ".join([
        ui.type_chip(cls.request_type),
        ui.urgency_pill(cls.urgency),
        ui.pill(ui.SOURCE_LABEL.get(cls.source, cls.source), "neutral", dot=False),
        ui.pill(f"sentiment: {cls.sentiment}", "neutral", dot=False),
    ])
    body = chips + ui.confidence_meter(cls.confidence, config.CONFIDENCE_THRESHOLD)
    body += f'<div class="op-rationale"><b>Rationale.</b> {cls.rationale}</div>'
    if memory_note:
        body += f'<div class="op-note mem">💾 <b>Memory.</b> {memory_note}</div>'
    if memory_impact and memory_impact.lower() not in ("none", "human decision", ""):
        body += (f'<div class="op-note">🔁 <b>Memory changed the plan.</b> '
                 f'{memory_impact}</div>')
    ui.card("🧠 Agent reasoning", body)


def render_plan(plan: list[PlannedStep], flags: list[str],
                title: str = "📋 Remediation plan"):
    rows = [{"key": s.key(), "reason": s.reason, "origin": s.origin} for s in plan]
    ui.card(title, ui.plan_table(rows))
    if flags:
        for f in flags:
            ui.flag(f"Guardrail: {f}")
    else:
        ui.flag("Agent's plan was fully policy-compliant — no repairs needed",
                ok=True)


def render_results(results, critic):
    st.markdown('<div class="op-card-title" style="font-size:0.74rem;font-weight:700;'
                'text-transform:uppercase;letter-spacing:0.08em;color:#898781;'
                'margin:6px 0 10px 0">⚙️ Execution timeline</div>',
                unsafe_allow_html=True)
    icon = {"SUCCESS": "✅", "FAILED": "❌", "SKIPPED": "⏭️"}
    for i, r in enumerate(results):
        artifact = r.artifact if isinstance(r.artifact, dict) else {}
        with st.expander(f"{icon.get(r.status, '•')} {i + 1} · {r.tool} — "
                         f"{r.status} · {r.executed_at}",
                         expanded=(r.tool == "draft_reply")):
            text = artifact.get("draft_text") or artifact.get("text")
            if text:
                meta = []
                if artifact.get("generated_by"):
                    meta.append(f"generated by {artifact['generated_by']}")
                if artifact.get("revised_by_critic"):
                    meta.append("✍️ revised by critic")
                ui.draft_block(text, " · ".join(meta))
                rest = {k: v for k, v in artifact.items()
                        if k not in ("draft_text", "text")}
                if rest:
                    st.json(rest, expanded=False)
            else:
                st.json(artifact)
    if critic:
        if not critic.get("available", True):
            ui.card("🪞 Critic verdict",
                    '<span style="color:#52514e">Critic unavailable — draft '
                    'shipped unreviewed.</span>')
        elif critic["approved"]:
            ui.card("🪞 Critic verdict",
                    ui.pill("draft approved", "good") +
                    '<span style="color:#52514e;font-size:0.88rem"> &nbsp;All '
                    'compliance checks passed.</span>')
        else:
            issues = "".join(f'<li>{i}</li>' for i in critic["issues"])
            ui.card("🪞 Critic verdict",
                    ui.pill("draft rejected & revised", "warning") +
                    f'<ul style="margin:10px 0 0 0;color:#52514e;'
                    f'font-size:0.88rem">{issues}</ul>')
            col1, col2 = st.columns(2)
            with col1:
                ui.draft_block(critic["original_text"], "original draft")
            with col2:
                ui.draft_block(critic["revised_text"], "revised draft (shipped)")


def render_run(run: AgentRun):
    render_reasoning(run.classification, run.memory_note, run.memory_impact)
    render_plan(run.final_plan, run.guardrail_flags)
    if str(run.final_status).replace("CaseStatus.", "") == "PENDING_REVIEW":
        st.markdown(
            f'<div class="op-card">{ui.status_pill("PENDING_REVIEW")}'
            f'<span style="color:#52514e;font-size:0.9rem"> &nbsp;Confidence '
            f'{run.classification.confidence:.2f} is below the threshold '
            f'({config.CONFIDENCE_THRESHOLD:.2f}) or approval is required — case '
            f'<b>{run.request.id}</b> is waiting in the <b>Human Review</b> tab. '
            f'Nothing was executed.</span></div>', unsafe_allow_html=True)
        return
    render_results(run.results, run.critic)
    follow_up = db.get_case(run.request.id).get("follow_up_at")
    tail = (f'<span style="color:#898781;font-size:0.85rem"> &nbsp;⏰ follow-up / '
            f'SLA: <code>{follow_up}</code></span>' if follow_up else "")
    st.markdown(f'<div class="op-card"><div class="op-card-title">Final status'
                f'</div>{ui.status_pill(run.final_status)}{tail}</div>',
                unsafe_allow_html=True)


def run_agent_with_status(req: IncomingRequest, **kwargs) -> AgentRun:
    with st.status(f"🛰️ Agent processing {req.id}…", expanded=True) as status:
        st.write("💾 Recalling sender memory…")
        st.write("🧠 Planning (classification + remediation plan)…")
        run = agent.process_request(req, autonomy_mode=autonomy_mode,
                                    critic_enabled=critic_enabled, **kwargs)
        st.write("🛡️ Plan validated against playbook policy")
        if run.results:
            st.write(f"⚙️ Executed {len(run.results)} steps")
        if run.critic:
            st.write("🪞 Critic review complete")
        final = str(run.final_status).replace("CaseStatus.", "").replace("_", " ")
        status.update(label=f"Agent run complete — {final}",
                      state="complete", expanded=False)
    return run


